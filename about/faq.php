<script type="text/javascript">
	var selected;

	$(".f9ecwp3_Tab_item a").on("click",function(e){
		var toShow = $(this).attr('data');
		selected = $("#"+toShow);

		if(selected.css("height") == "0px"){
			selected.css("height",selected[0].scrollHeight+"px");
			$(this).addClass("show");
		}else{
			selected.css({"height":"0px","overflow":"hidden"});
			$(this).removeClass("show");
		}

	});

	//Amazin' *_*
	$(".faqTab_listitem a").on("click",function(e){
		var toShow = $(this).attr('data');
		selected = $("#"+toShow);
		if(!selected.hasClass("active")){
			$(".faqPage_f9ecwp3_Tab").removeClass("shown");
			$(".faqPage_f9ecwp3_Tab").one('webkitTransitionEnd otransitionend oTransitionEnd msTransitionEnd transitionend',   function(e) {
				if(e.target === e.currentTarget){
					$(".faqPage_f9ecwp3_Tab").removeClass("active");
				selected.addClass("active");
				selected.addClass("shown");
				}
  			});

		}
	});

</script>
